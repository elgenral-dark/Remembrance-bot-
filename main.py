def update_streak(user: dict) -> bool:
    """
    تحديث سلسلة الأيام بعد تسجيل ذكر جديد.
    تُرجع True لو زادت السلسلة (يعني يوم جديد دخل في السلسلة).
    """
    today = today_str()
    last = user.get("last_active_date", "")
    if last == today:
        return False  # تم الاحتساب بالفعل اليوم
    if last:
        # لو آخر نشاط كان أمس → نزيد السلسلة، وإلا نبدأ من 1
        yesterday = (now_in_default_tz().date() - timedelta(days=1)).isoformat()
        if last == yesterday:
            user["streak_days"] = user.get("streak_days", 0) + 1
        else:
            user["streak_days"] = 1
    else:
        # أول مرة يُسجِّل فيها ذكراً
        user["streak_days"] = 1
    user["last_active_date"] = today
    return True


def maybe_award_badges(user: dict, context: str = "") -> list:
    """
    فحص الشارات ومنح أي شارة جديدة استحقها المستخدم.
    `context` تلميح اختياري ("morning", "evening", ...) لمنح شارات السياق.
    تُرجع قائمة معرّفات الشارات الممنوحة في هذه المكالمة.
    """
    awarded = []
    have = set(user.get("badges", []))

    def grant(badge_id: str):
        """دالة داخلية: تمنح شارة لو لم يحصل عليها من قبل."""
        if badge_id not in have:
            user.setdefault("badges", []).append(badge_id)
            have.add(badge_id)
            awarded.append(badge_id)

    # شارات حسب عدد الأذكار
    score = user.get("score", 0)
    if score >= 1:
        grant("first_zikr")
    if score >= 100:
        grant("100_azkar")
    if score >= 1000:
        grant("1000_azkar")

    # شارات السلسلة
    streak = user.get("streak_days", 0)
    if streak >= 7:
        grant("week_streak")
    if streak >= 30:
        grant("month_streak")

    # شارات حسب السياق
    if context == "morning":
        grant("morning_lover")
    if context == "evening":
        grant("evening_lover")

    # شارات حسب الوقت
    hour = now_in_default_tz().hour
    if 0 <= hour < 5:
        grant("night_owl")  # قائم الليل
    if 4 <= hour < 6:
        grant("early_bird")  # المبكّر

    return awarded


