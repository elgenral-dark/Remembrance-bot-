# ---- الرد الافتراضي على أي رسالة لا تطابق أمراً --------------------------
@bot.message_handler(func=lambda m: True)
def handle_fallback(message: types.Message) -> None:
    """ردّ افتراضي عند كتابة المستخدم لرسالة لا يفهمها البوت."""
    # 1) لو الأدمن في حالة انتظار إدخال (بحث/بث/منح نقاط...) نُمرّر له الرسالة أولاً
    if _handle_admin_input(message):
        return

    # 2) لو المستخدم محظور نتجاهله بصمت
    with data_lock:
        if users_data.get(str(message.chat.id), {}).get("banned"):
            return

    # 3) لو البوت في وضع صيانة نعرض رسالة الصيانة (لغير الأدمن)
    if maintenance["on"] and not is_admin(message.from_user.id):
        bot.send_message(message.chat.id, maintenance["msg"])
        return

    bot.send_message(
        message.chat.id,
        "اختر من الأزرار بالأسفل أو أرسل /help لرؤية كل الأوامر 🌙",
        reply_markup=main_keyboard(),
    )


