def filter_azkar(azkar_type: str) -> list:
    """تُرجع الأذكار المطابقة للنوع المطلوب ('all' تُرجع كل القائمة)."""
    if azkar_type == "all":
        return AZKAR
    if azkar_type == "tasbeeh":
        return [z for z in AZKAR if z["type"] in ("tasbeeh", "tahlil", "salah")]
    if azkar_type == "istighfar":
        return [z for z in AZKAR if z["type"] == "istighfar"]
    if azkar_type == "dua":
        return [z for z in AZKAR if z["type"] == "dua"]
    return AZKAR


