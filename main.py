def fmt_uptime() -> str:
    """تنسيق الوقت منذ تشغيل البوت بصيغة عربية مختصرة."""
    secs = int(time.time() - BOT_START_TS)
    d, secs = divmod(secs, 86400)
    h, secs = divmod(secs, 3600)
    m, _ = divmod(secs, 60)
    return f"{d}ي {h}س {m}د"


def fmt_size(path: str) -> str:
    """حجم الملف بالكيلوبايت أو 'لا يوجد'."""
    try:
        kb = os.path.getsize(path) / 1024
        if kb < 1024:
            return f"{kb:.1f} KB"
        return f"{kb / 1024:.2f} MB"
    except OSError:
        return "—"


def fmt_dt(ts: float) -> str:
    """تنسيق طابع زمني إلى تاريخ ووقت قابلَين للقراءة."""
    if not ts:
        return "غير معروف"
    try:
        return datetime.fromtimestamp(ts, DEFAULT_TZ).strftime("%Y-%m-%d %H:%M")
    except Exception:
        return "—"


