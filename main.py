def get_level_index(score: int) -> int:
    """تُرجع رقم المستوى المناسب لعدد النقاط."""
    idx = 0
    for i, (threshold, _) in enumerate(LEVELS):
        if score >= threshold:
            idx = i
        else:
            break
    return idx


def get_level_label(score: int) -> str:
    """تُرجع اسم المستوى الحالي للمستخدم."""
    return LEVELS[get_level_index(score)][1]


def get_next_level_target(score: int):
    """تُرجع عدد النقاط المطلوبة للمستوى التالي، أو None لو في الأعلى."""
    idx = get_level_index(score)
    if idx + 1 >= len(LEVELS):
        return None
    return LEVELS[idx + 1][0]


