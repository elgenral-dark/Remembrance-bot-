        # تخطي إذا أرسلنا تذكير هذه الصلاة اليوم
        if last_keys.get(prayer) == today_iso:
            continue
        delta = (when_utc - now_utc).total_seconds() / 60.0
        if 0 <= delta <= PRAYER_REMINDER_LEAD_MIN:
            ar = PRAYER_AR.get(prayer, prayer)
            local_str = when_utc.astimezone(DEFAULT_TZ).strftime("%H:%M")
            safe_send(
                chat_id,
                f"🕋 <b>تذكير صلاة {ar}</b>\n"
                f"اقتربت صلاة {ar} (تقريبًا الساعة {local_str})\n"
                f"اللهم أعنا على ذكرك وشكرك وحسن عبادتك.",
            )
            last_keys[prayer] = today_iso  # حتى لا نُكرر التذكير


