def _bcast_recipients(target: str) -> list:
    """يُرجع قائمة chat_ids بناءً على الفئة المستهدفة."""
    week_ago = (now_in_default_tz().date() - timedelta(days=7)).isoformat()
    with data_lock:
        ids = []
        for cid, u in users_data.items():
            if u.get("banned"):
                continue
            if target == "all":
                ids.append(int(cid))
            elif target == "sub" and u.get("subscribed"):
                ids.append(int(cid))
            elif target == "city" and u.get("city"):
                ids.append(int(cid))
            elif target == "active" and u.get("last_active_date", "") >= week_ago:
                ids.append(int(cid))
    return ids


