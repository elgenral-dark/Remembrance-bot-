    if not os.path.exists(EXTRA_CONTENT_FILE):
        return
    try:
        with open(EXTRA_CONTENT_FILE, "r", encoding="utf-8") as f:
            extra = json.load(f)
        for z in extra.get("azkar", []):
            AZKAR.append({"type": "all", "text": z})
        for d in extra.get("adiyah", []):
            ADIYAH.append(d)
        for a in extra.get("ayat", []):
            AYAT.append(a)
        for h in extra.get("ahadith", []):
            AHADITH.append(h)
        n = sum(len(v) for v in extra.values() if isinstance(v, list))
        if n:
            print(f"[extra_content] تم تحميل {n} عنصر إضافي")
    except Exception as e:
        print(f"[extra_content] خطأ: {e}")


