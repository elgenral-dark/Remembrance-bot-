# المُجَدوِل (Background Scheduler)
# ---------------------------------------------------------------------------
def scheduler_loop() -> None:
    """
    حلقة لا نهائية تعمل في خيط (thread) منفصل، وكل دقيقة تقوم بـ:
      • إرسال الأذكار الدورية للمشتركين (مع احترام المدة والوضع الصامت)
      • إرسال أذكار الصباح/المساء في موعدها (مرة واحدة يومياً)
      • إرسال تذكير الصلاة لمن ضبط مدينته
    """
    while True:
        try:
            now_local = now_in_default_tz()
            now_ts = time.time()

            # نأخذ نسخة من البيانات لتجنّب القفل أثناء التكرار الطويل
            with data_lock:
                snapshot = [(int(cid), info.copy()) for cid, info in users_data.items()]

            for chat_id, info in snapshot:
                # تجاهل المستخدمين المحظورين تماماً
                if info.get("banned"):
                    continue
                # في وضع الصيانة لا نُرسل أي رسائل تلقائية
                if maintenance["on"]:
                    continue

                # 1) الأذكار الدورية
                if info.get("subscribed"):
                    interval = max(1, info.get("interval_hours", 1))
                    quiet = info.get("quiet_hours", True) and is_quiet_hours()
                    elapsed = now_ts - info.get("last_sent_at", 0)
                    if not quiet and elapsed >= interval * 3600:
                        send_zikr(chat_id)

                # 2) أذكار الصباح
                if info.get("morning_azkar") and now_local.hour == MORNING_HOUR:
                    if info.get("last_morning_date") != now_local.date().isoformat():
                        send_morning_azkar(chat_id)
                        with data_lock:
                            get_user(chat_id)["last_morning_date"] = (
                                now_local.date().isoformat()
                            )
                            save_data(users_data)

                # 3) أذكار المساء
                if info.get("evening_azkar") and now_local.hour == EVENING_HOUR:
                    if info.get("last_evening_date") != now_local.date().isoformat():
                        send_evening_azkar(chat_id)
                        with data_lock:
                            get_user(chat_id)["last_evening_date"] = (
                                now_local.date().isoformat()
                            )
                            save_data(users_data)

                # 4) تذكير الصلاة
                if info.get("city"):
                    with data_lock:
                        maybe_send_prayer_reminder(chat_id, get_user(chat_id))
                        save_data(users_data)
        except Exception as e:
            print(f"[scheduler_loop] خطأ: {e}")

        # ننتظر دقيقة قبل الجولة التالية
        time.sleep(60)


