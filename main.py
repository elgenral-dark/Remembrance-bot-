def load_data() -> dict:
    """تحميل بيانات المستخدمين من ملف JSON، مع تحديث السجلات القديمة."""
    if not os.path.exists(DATA_FILE):
        return {}
    try:
        with open(DATA_FILE, "r", encoding="utf-8") as f:
            data = json.load(f)
    except (json.JSONDecodeError, OSError):
        # لو الملف تالف أو لا يمكن قراءته نبدأ بقاعدة بيانات فارغة
        return {}
    # ترقية السجلات القديمة لإضافة الحقول الجديدة (لو حدّثنا الكود)
    for key, info in list(data.items()):
        defaults = default_user()
        for k, v in defaults.items():
            info.setdefault(k, v)
    return data


def save_data(data: dict) -> None:
    """حفظ البيانات في ملف JSON بشكل آمن (نكتب أولاً في ملف مؤقت ثم نستبدله)."""
    tmp = DATA_FILE + ".tmp"
    with open(tmp, "w", encoding="utf-8") as f:
        json.dump(data, f, ensure_ascii=False, indent=2)
    os.replace(tmp, DATA_FILE)


