@bot.message_handler(commands=["city"])
def handle_city(message: types.Message) -> None:
    """ضبط مدينة المستخدم لتفعيل تذكير الصلاة."""
    parts = (message.text or "").split(maxsplit=2)
    if len(parts) < 3:
        bot.send_message(
            message.chat.id,
            "اكتب الأمر بهذا الشكل:\n<code>/city Cairo Egypt</code>",
        )
        return
    city, country = parts[1].strip(), parts[2].strip()
    timings = fetch_prayer_times(city, country, now_in_default_tz().date())
    if not timings:
        bot.send_message(
            message.chat.id,
            "❌ لم أتمكن من إيجاد المدينة. تأكد من الكتابة بالإنجليزية.",
        )
        return
    with data_lock:
        get_user(message.chat.id)["city"] = {"city": city, "country": country}
        save_data(users_data)

    # نعرض أوقات الصلوات اليوم تأكيداً للمستخدم
    lines = [f"✅ تم ضبط مدينتك: <b>{city}, {country}</b>", "", "🕋 أوقات صلاة اليوم:"]
    for prayer in ("Fajr", "Dhuhr", "Asr", "Maghrib", "Isha"):
        if prayer in timings:
            local = timings[prayer].astimezone(DEFAULT_TZ).strftime("%H:%M")
            lines.append(f"• {PRAYER_AR[prayer]}: <b>{local}</b>")
    lines.append("\nسيصلك تذكير قبل كل صلاة بعشر دقائق بإذن الله.")
    bot.send_message(message.chat.id, "\n".join(lines))


