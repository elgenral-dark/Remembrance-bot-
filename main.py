def fetch_prayer_times(city: str, country: str, day: date) -> dict:
    """
    جلب أوقات الصلاة لمدينة معيّنة في يوم محدّد.
    تُرجع dict يربط اسم الصلاة بوقتها (datetime بـ UTC).
    تستخدم ذاكرة مؤقتة (cache) لتقليل الطلبات على الإنترنت.
    """
    cache_key = (city.lower(), country.lower(), day.isoformat())
    if cache_key in prayer_times_cache:
        return prayer_times_cache[cache_key]

    url = "https://api.aladhan.com/v1/timingsByCity"
    params = {
        "city": city,
        "country": country,
        "method": 5,  # طريقة الهيئة المصرية العامة للمساحة (شائعة)
        "date": day.strftime("%d-%m-%Y"),
    }
    try:
        resp = requests.get(url, params=params, timeout=15)
        resp.raise_for_status()
        payload = resp.json()
        timings = payload["data"]["timings"]
        tz_name = payload["data"]["meta"]["timezone"]

        # محاولة استخدام المنطقة الزمنية الفعلية للمدينة
        try:
            from zoneinfo import ZoneInfo

            tz = ZoneInfo(tz_name)
        except Exception:
            tz = DEFAULT_TZ

        result = {}
        for prayer in ("Fajr", "Dhuhr", "Asr", "Maghrib", "Isha"):
            t = timings.get(prayer)
            if not t:
                continue
            # حذف أي لاحقة مثل "(EET)" من الوقت
            t = t.split(" ")[0]
            hh, mm = map(int, t.split(":"))
            local = datetime(day.year, day.month, day.day, hh, mm, tzinfo=tz)
            # نخزّن كل الأوقات بـ UTC حتى تكون المقارنة سهلة
            result[prayer] = local.astimezone(timezone.utc)

        prayer_times_cache[cache_key] = result
        return result
    except Exception as e:
        print(f"[fetch_prayer_times] خطأ في {city},{country}: {e}")
        return {}


