# ---- معالج الإدخال النصي للأدمن (للحالات التي تنتظر نصاً) ------------------
def _handle_admin_input(message: types.Message) -> bool:
    """
    يعالج الإدخال النصي عندما يكون الأدمن في حالة انتظار.
    تُرجع True لو تم استهلاك الرسالة (لمنع تمريرها للـfallback).
    """
    uid = message.from_user.id
    if not is_admin(uid):
        return False
    state = admin_states.get(uid)
    if not state:
        return False

    text = (message.text or "").strip()
    if text == "/cancel":
        admin_states.pop(uid, None)
        bot.send_message(message.chat.id, "❌ تم الإلغاء.", reply_markup=admin_main_kb())
        return True

    action = state.get("action")

    # --- بحث عن مستخدم
    if action == "find_user":
        admin_states.pop(uid, None)
        if not text.lstrip("-").isdigit():
            bot.send_message(message.chat.id, "❌ ID غير صحيح. يجب أن يكون رقماً.", reply_markup=back_kb())
            return True
        _show_user_card(message.chat.id, text)
        return True

    # --- مراسلة مستخدم: الخطوة 1 (الـID)
    if action == "msg_step1":
        if not text.lstrip("-").isdigit():
            bot.send_message(message.chat.id, "❌ ID غير صحيح.", reply_markup=back_kb())
            return True
        admin_states[uid] = {"action": "msg_user", "target": text}
        bot.send_message(message.chat.id, f"✉️ الآن أرسل نص الرسالة للمستخدم <code>{text}</code>:")
        return True

    # --- مراسلة مستخدم: نص الرسالة
    if action == "msg_user":
        target = state.get("target")
        admin_states.pop(uid, None)
        full = "📩 <b>رسالة من إدارة البوت:</b>\n\n" + text
        ok = safe_send(int(target), full)
        bot.send_message(
            message.chat.id,
            "✅ أُرسلت الرسالة." if ok else "❌ فشل الإرسال (ربما المستخدم حظر البوت).",
            reply_markup=admin_main_kb(),
        )
        return True

    # --- منح نقاط: الخطوة 1 (الـID)
    if action == "grant_step1":
        if not text.lstrip("-").isdigit():
            bot.send_message(message.chat.id, "❌ ID غير صحيح.", reply_markup=back_kb())
            return True
        admin_states[uid] = {"action": "grant_points", "target": text}
        bot.send_message(message.chat.id, f"🎁 الآن أرسل عدد النقاط للمستخدم <code>{text}</code> (يمكن سالب):")
        return True

    # --- منح نقاط: العدد
    if action == "grant_points":
        target = state.get("target")
        admin_states.pop(uid, None)
        try:
            pts = int(text)
        except ValueError:
            bot.send_message(message.chat.id, "❌ أدخل رقماً صحيحاً.", reply_markup=back_kb())
            return True
        with data_lock:
            u = users_data.get(str(target))
            if not u:
                bot.send_message(message.chat.id, "❌ المستخدم غير موجود.", reply_markup=back_kb())
                return True
            u["score"] = max(0, u.get("score", 0) + pts)
            new_score = u["score"]
            save_data(users_data)
        sign = "+" if pts >= 0 else ""
        bot.send_message(
            message.chat.id,
            f"✅ تم. الرصيد الجديد لـ <code>{target}</code>: <b>{new_score}</b> ({sign}{pts})",
            reply_markup=admin_main_kb(),
        )
        return True

    # --- بث: نص الرسالة → عرض المعاينة وطلب التأكيد
    if action == "bcast_text":
        target = state.get("target")
        ids = _bcast_recipients(target)
        admin_states[uid] = {"action": "bcast_confirm", "target": target, "text": text}
        label = {"all": "جميع المستخدمين", "sub": "المشتركون", "city": "أصحاب المدن", "active": "النشطون أسبوعياً"}[target]
        preview = (
            "📢 <b>معاينة البث</b>\n"
            f"🎯 الفئة: <b>{label}</b>\n"
            f"👥 العدد: <b>{len(ids)}</b>\n"
            "━━━━━━━━━━━━━━━━━━\n"
            "📩 <b>رسالة من إدارة البوت:</b>\n\n"
            f"{text}\n"
            "━━━━━━━━━━━━━━━━━━"
        )
        bot.send_message(message.chat.id, preview, reply_markup=confirm_kb("adm_bcgo_x"))
        return True

    # --- إضافة محتوى
    if action == "add_zikr":
        admin_states.pop(uid, None)
        AZKAR.append({"type": "all", "text": text})
        append_extra_content("azkar", text)
        bot.send_message(message.chat.id, f"✅ تمت إضافة الذكر. الإجمالي الآن: <b>{len(AZKAR)}</b>", reply_markup=admin_main_kb())
        return True

    if action == "add_dua":
        admin_states.pop(uid, None)
        ADIYAH.append(text)
        append_extra_content("adiyah", text)
        bot.send_message(message.chat.id, f"✅ تمت إضافة الدعاء. الإجمالي الآن: <b>{len(ADIYAH)}</b>", reply_markup=admin_main_kb())
        return True

    if action == "add_ayah":
        admin_states.pop(uid, None)
        parts = text.split("\n", 2)
        if len(parts) < 3:
            bot.send_message(message.chat.id, "❌ يجب 3 أسطر: الآية، المرجع، التأمّل.", reply_markup=back_kb())
            return True
        item = {"ayah": parts[0].strip(), "ref": parts[1].strip(), "tafseer": parts[2].strip()}
        AYAT.append(item)
        append_extra_content("ayat", item)
        bot.send_message(message.chat.id, f"✅ تمت إضافة الآية. الإجمالي: <b>{len(AYAT)}</b>", reply_markup=admin_main_kb())
        return True

    if action == "add_hadith":
        admin_states.pop(uid, None)
        parts = text.split("\n", 2)
        if len(parts) < 3:
            bot.send_message(message.chat.id, "❌ يجب 3 أسطر: الحديث، المرجع، الشرح.", reply_markup=back_kb())
            return True
        item = {"text": parts[0].strip(), "ref": parts[1].strip(), "shar7": parts[2].strip()}
        AHADITH.append(item)
        append_extra_content("ahadith", item)
        bot.send_message(message.chat.id, f"✅ تمت إضافة الحديث. الإجمالي: <b>{len(AHADITH)}</b>", reply_markup=admin_main_kb())
        return True

    # --- تعديل رسالة الصيانة
    if action == "set_maintmsg":
        admin_states.pop(uid, None)
        maintenance["msg"] = text
        save_admin_config()
        bot.send_message(message.chat.id, "✅ تم تحديث رسالة الصيانة.", reply_markup=admin_main_kb())
        return True

    return False


