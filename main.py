@bot.message_handler(content_types=["document"])
def handle_admin_document(message: types.Message) -> None:
    """يستقبل ملف نسخة احتياطية من الأدمن لاستعادة البيانات."""
    uid = message.from_user.id
    state = admin_states.get(uid)
    if not is_admin(uid) or not state or state.get("action") != "restore":
        return
    try:
        file_info = bot.get_file(message.document.file_id)
        raw = bot.download_file(file_info.file_path)
        data = json.loads(raw.decode("utf-8"))
        if not isinstance(data, dict):
            raise ValueError("الملف ليس بصيغة صحيحة")
        # نسخة احتياطية من الموجود قبل الاستبدال
        if os.path.exists(DATA_FILE):
            os.replace(DATA_FILE, DATA_FILE + ".bak")
        with data_lock:
            users_data.clear()
            users_data.update(data)
            # ترقية الحقول للسجلات الجديدة
            for info in users_data.values():
                for k, v in default_user().items():
                    info.setdefault(k, v)
            save_data(users_data)
        admin_states.pop(uid, None)
        bot.send_message(
            message.chat.id,
            f"✅ تم استرجاع <b>{len(data)}</b> مستخدم بنجاح.\n"
            "تم حفظ النسخة القديمة في <code>users_data.json.bak</code>",
            reply_markup=admin_main_kb(),
        )
    except Exception as e:
        bot.send_message(message.chat.id, f"❌ فشل الاسترجاع: {e}", reply_markup=back_kb())


