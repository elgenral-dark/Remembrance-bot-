    if not is_admin(call.from_user.id):
        return
    state = admin_states.get(call.from_user.id, {})
    if state.get("action") != "bcast_confirm":
        return bot.answer_callback_query(call.id, "❌ انتهت الجلسة")
    target = state.get("target")
    text = state.get("text", "")
    full = "📢 <b>رسالة من إدارة البوت:</b>\n\n" + text
    ids = _bcast_recipients(target)
    _safe_edit(call, f"⏳ جاري الإرسال إلى <b>{len(ids)}</b> مستخدم...", None)

    sent = failed = 0
    for cid in ids:
        if safe_send(cid, full):
            sent += 1
        else:
            failed += 1
        time.sleep(0.05)
    admin_states.pop(call.from_user.id, None)
    bot.send_message(
        call.message.chat.id,
        f"✅ <b>اكتمل البث</b>\n\n"
        f"• أُرسلت إلى: <b>{sent}</b>\n"
        f"• فشلت لـ: <b>{failed}</b>\n"
        f"• الإجمالي المستهدف: <b>{len(ids)}</b>",
        reply_markup=admin_main_kb(),
    )


