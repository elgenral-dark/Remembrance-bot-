def now_in_default_tz() -> datetime:
    """يُرجع الوقت الحالي بالمنطقة الزمنية الافتراضية (UTC+3)."""
    return datetime.now(DEFAULT_TZ)


def today_str() -> str:
    """يُرجع تاريخ اليوم بصيغة YYYY-MM-DD."""
    return now_in_default_tz().date().isoformat()


def is_quiet_hours() -> bool:
    """هل نحن داخل نافذة الصمت الليلي حالياً؟"""
    h = now_in_default_tz().hour
    if QUIET_START <= QUIET_END:
        # حالة عادية: مثلاً من 0 إلى 6
        return QUIET_START <= h < QUIET_END
    # حالة تعبر منتصف الليل (مثل 22 إلى 6) — مدعومة لكن لا نستخدمها هنا
    return h >= QUIET_START or h < QUIET_END


def pick_random_zikr(last_zikr: str, azkar_type: str) -> str:
    """اختيار ذكر عشوائي مختلف عن السابق."""
    pool = filter_azkar(azkar_type) or AZKAR
    if len(pool) == 1:
        return pool[0]["text"]
    # نظل نختار حتى نضمن عدم تكرار آخر ذكر
    while True:
        candidate = random.choice(pool)["text"]
        if candidate != last_zikr:
            return candidate


def pick_random_index(length: int, last_idx: int) -> int:
    """اختيار رقم عشوائي في النطاق [0, length) يختلف عن last_idx."""
    if length <= 1:
        return 0
    while True:
        idx = random.randint(0, length - 1)
        if idx != last_idx:
            return idx


