# 📚 ميزة القرآن الكريم — قائمة السور + عرض الآيات مع التصفح
# ===========================================================================
# نستخدم API مفتوح: alquran.cloud لجلب نص السور بالرسم العثماني.
# نخزّن السور في الذاكرة بعد أول جلب لتسريع الاستجابة وتقليل الطلبات.
# ===========================================================================

SURAH_CACHE: dict = {}                 # {رقم السورة: قائمة الآيات بالنصّ}
SURAHS_PER_PAGE = 12                   # كم سورة في كل صفحة من قائمة السور
AYAT_PER_PAGE = 8                      # كم آية في كل صفحة عرض (مع مراعاة 4096 حرف)
QURAN_API_URL = "https://api.alquran.cloud/v1/surah/{n}/quran-uthmani"


def _fetch_surah(num: int) -> list:
    """جلب آيات سورة ما من الإنترنت، مع تخزينها مؤقتاً في الذاكرة."""
    if num in SURAH_CACHE:
        return SURAH_CACHE[num]
    try:
        resp = requests.get(QURAN_API_URL.format(n=num), timeout=20)
        resp.raise_for_status()
        ayat = [a["text"] for a in resp.json()["data"]["ayahs"]]
        SURAH_CACHE[num] = ayat
        return ayat
    except Exception as e:
        print(f"[fetch_surah {num}] {e}")
        return []


def _quran_list_keyboard(page: int = 0) -> types.InlineKeyboardMarkup:
    """بناء لوحة أزرار قائمة السور لصفحة معيّنة."""
    kb = types.InlineKeyboardMarkup(row_width=2)
    total_pages = (114 + SURAHS_PER_PAGE - 1) // SURAHS_PER_PAGE
    page = max(0, min(page, total_pages - 1))
    start = page * SURAHS_PER_PAGE
    end = min(start + SURAHS_PER_PAGE, 114)

    # أزرار السور (صفّان لكل سطر)
    row = []
    for i in range(start, end):
        n = i + 1
        row.append(types.InlineKeyboardButton(
            f"{n}. {SURAH_NAMES[i]}",
            callback_data=f"qsur_{n}_0",
        ))
        if len(row) == 2:
            kb.row(*row)
            row = []
    if row:
        kb.row(*row)

    # أزرار التصفّح
    nav = []
    if page > 0:
        nav.append(types.InlineKeyboardButton("⬅️ السابق", callback_data=f"qpage_{page-1}"))
    nav.append(types.InlineKeyboardButton(f"{page+1}/{total_pages}", callback_data="qnoop"))
    if page < total_pages - 1:
        nav.append(types.InlineKeyboardButton("التالي ➡️", callback_data=f"qpage_{page+1}"))
    kb.row(*nav)
    return kb


def _build_surah_page(num: int, page: int):
    """إعداد نص الصفحة + لوحة الأزرار لسورة معيّنة عند صفحة آيات معيّنة."""
    ayat = _fetch_surah(num)
    if not ayat:
        return None, None

    name = SURAH_NAMES[num - 1]
    total_pages = (len(ayat) + AYAT_PER_PAGE - 1) // AYAT_PER_PAGE
    page = max(0, min(page, total_pages - 1))
    start = page * AYAT_PER_PAGE
    end = min(start + AYAT_PER_PAGE, len(ayat))

    text = (
        f"📖 <b>سورة {name}</b>\n"
        f"الآيات {start + 1} – {end} من {len(ayat)}  |  صفحة {page + 1}/{total_pages}\n"
        f"{'─' * 20}\n\n"
    )
    for i, a in enumerate(ayat[start:end], start + 1):
        text += f"{a}  ﴿{i}﴾\n\n"

    # ضمان عدم تجاوز حد تيليجرام (4096 حرف)
    if len(text) > 4000:
        text = text[:3990] + "\n…"

    kb = types.InlineKeyboardMarkup(row_width=3)
    nav = []
    if page > 0:
        nav.append(types.InlineKeyboardButton("⬅️ السابق", callback_data=f"qsur_{num}_{page-1}"))
    nav.append(types.InlineKeyboardButton(f"{page+1}/{total_pages}", callback_data="qnoop"))
    if page < total_pages - 1:
        nav.append(types.InlineKeyboardButton("التالي ➡️", callback_data=f"qsur_{num}_{page+1}"))
    kb.row(*nav)
    kb.add(types.InlineKeyboardButton("📚 العودة لقائمة السور", callback_data="qpage_0"))
    return text, kb


